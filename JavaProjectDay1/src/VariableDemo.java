
public class VariableDemo {

	public static void main(String[] args) {
		int intVar;
		long longVar;
		float floatVar;
		double doubleVar;
		char charVar;
		boolean booleanVar;
		
		intVar = 10;
		longVar = 100000000000l;
		floatVar = 12.34f;
		doubleVar = 23.45;
		charVar = 'B';
		booleanVar = true;
		
		System.out.println(intVar+" "+longVar+ " "+floatVar+ " "+doubleVar+ " "+charVar+ " "+booleanVar);
				
	}

}
